    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Document doc = Jsoup.connect(JtxtURL.getText()).get();
            Elements forms = Dom.getForms(doc);   
            String data ="";
            for (Element fields : forms) {                
                data = data + Dom.getFieldsForm(fields).toString();                                                           
            }
            JtxtResults.setText(data);     
        } catch (Exception e) {
            JtxtResults.setText(e.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

